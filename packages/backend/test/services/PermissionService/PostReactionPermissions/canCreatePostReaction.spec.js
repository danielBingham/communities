const Logger = require('../../../../logger')
const FeatureFlags = require('../../../../features')

const ServiceError = require('../../../../errors/ServiceError')
const PermissionService = require('../../../../services/PermissionService')
const PostReactionPermissions = require('../../../../services/permission/PostReactionPermissions')

const entities = require('../../../fixtures/entities')
const database = require('../../../fixtures/database')

describe("PostReactionPermissions.canCreatePostReaction()", function() {

    const core = {
        logger: new Logger(),
        config: {
            s3: {
                bucket_url: '',
                access_id: '',
                access_key: '',
                bucket: ''
            },
        },
        database: {
            query: jest.fn()
        },
        queue: null,
        postmarkClient: {
            sendEmail: jest.fn()
        },
        features: new FeatureFlags() 
    }

    beforeEach(function() {
        core.database.query.mockReset()
        // Disable logging.
        core.logger.level = -1 
    })

    it("Should allow anyone to create a PostReaction", async function() {
        const service = new PermissionService(core)

        const currentUser = entities.users.dictionary['5c44ce06-1687-4709-b67e-de76c05acb6a']

        const canCreatePostReaction = await service.can(currentUser, 'create', 'PostReaction')

        expect(canCreatePostReaction).toBe(true)
    })
})
